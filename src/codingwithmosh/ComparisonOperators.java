package codingwithmosh;

public class ComparisonOperators {

    public static void main(String[] args) {
        int x = 1;
        int y = 1;
        int a = 1;
        int b = 2;
        // the "==" compares the vale if it's true or not
        // in simple words this is a boolean expression
        // (true or false)
        System.out.println(x == y);

        // x is not equals to y returns false
        System.out.println(x != y);

        System.out.println(a > b);
                // or
        System.out.println(a <= b);
    }
}
